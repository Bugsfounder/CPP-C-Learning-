#include <iostream>

using namespace std;

int main(int argc, char const *argv[])
{
    // '<<' This is called incersion operator
    // '>>' This is called Extraction Operator

    int num1 , num2 ;

    cout <<"Enter The Value of Number 1: ";
    cin>>num1;

    cout <<"Enter The Value of Number 2: ";
    cin>>num2;

    cout<<"The sum of "<<num1<<" and "<<num2<<" is: "<<num2+num1;

    return 0;
}
